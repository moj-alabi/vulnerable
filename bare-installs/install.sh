# ═════════════════════════════════════════════════════════════════════════════
#  SECTION 11 – Finalize nginx config
# ═════════════════════════════════════════════════════════════════════════════
finalize_nginx() {
    info "Finalising nginx configuration…"

    local SNIPPET_DIR="/etc/nginx/snippets"
    mkdir -p "$SNIPPET_DIR"
    cp "${NGINX_SITES}/vuln-apps.conf" "${SNIPPET_DIR}/vuln-apps.conf"

    # Try to detect the main server block file (assume it's the default site)
    local MAIN_CONF
    for F in \
        /etc/nginx/sites-enabled/default \
        /etc/nginx/sites-available/default \
        /etc/nginx/conf.d/default.conf; do
        [[ -f "$F" ]] && { MAIN_CONF="$F"; break; }
    done

    if [[ -n "${MAIN_CONF:-}" ]]; then
        if grep -q "vuln-apps.conf" "$MAIN_CONF"; then
            warn "nginx already includes vuln-apps snippet – skipping"
        else
            # Insert include just before the closing brace of the first server block
            sed -i '/^}/i\    include snippets\/vuln-apps.conf;' "$MAIN_CONF"
            success "Inserted snippet include into $MAIN_CONF"
        fi
    else
        warn "Could not auto-detect main nginx server block."
        warn "Please add the following line inside your server {} block manually:"
        warn "    include snippets/vuln-apps.conf;"
    fi

    nginx -t && systemctl reload nginx && success "nginx reloaded"
}

# ═════════════════════════════════════════════════════════════════════════════
#  MAIN
# ═════════════════════════════════════════════════════════════════════════════
main() {
    echo ""
    echo -e "${CYAN}╔══════════════════════════════════════════════════╗${NC}"
    echo -e "${CYAN}║   Vulnerable Apps – Bare-Metal Installer          ║${NC}"
    echo -e "${CYAN}╚══════════════════════════════════════════════════╝${NC}"
    echo ""

    # Remove old snippet so we start fresh on each run
    rm -f "${NGINX_SITES}/vuln-apps.conf"

    section_deps
    section_mysql

    install_mutillidae
    install_bwapp
    install_wackopicko
    install_hackazon

    install_webgoat
    install_dvna
    install_juiceshop
    install_wrongsecrets

    finalize_nginx

    echo ""
    echo -e "${GREEN}════════════════════════════════════════════════════${NC}"
    echo -e "${GREEN}  Installation complete!  Summary:${NC}"
    echo -e "${GREEN}════════════════════════════════════════════════════${NC}"
    local IP
    IP=$(hostname -I | awk '{print $1}')
    echo ""
    echo -e "  ${CYAN}PHP apps (served directly by nginx)${NC}"
    echo -e "  ● Mutillidae II   →  http://${IP}/mutillidae"
    echo -e "  ● bWAPP           →  http://${IP}/bwapp  (visit /bwapp/install.php first)"
    echo -e "  ● WackoPicko      →  http://${IP}/wackopicko"
    echo -e "  ● Hackazon        →  http://${IP}/hackazon"
    echo ""
    echo -e "  ${CYAN}Standalone apps (reverse-proxied through nginx)${NC}"
    echo -e "  ● WebGoat 2023    →  http://${IP}/WebGoat   (Java, ~60 s startup)"
    echo -e "  ● DVNA            →  http://${IP}/dvna"
    echo -e "  ● Juice Shop      →  http://${IP}/juice-shop/"
    echo -e "  ● WrongSecrets    →  http://${IP}/wrongsecrets"
    echo ""
    echo -e "  ${YELLOW}Your existing DVWA should still be at http://${IP}/DVWA${NC}"
    echo ""
    echo -e "  Full log: ${LOG_FILE}"
    echo ""
}

main "$@"
