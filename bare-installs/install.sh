#!/usr/bin/env bash
# =============================================================================
#  Vulnerable Web Apps – Bare-Metal Installer (no Docker)
#  Installs alongside an existing nginx + DVWA setup.
#
#  Apps installed
#  ─────────────────────────────────────────────────────
#  PHP  (served via nginx at  http://<ip>/<app>)
#    • Mutillidae II        → /mutillidae
#    • bWAPP                → /bwapp
#    • WackoPicko           → /wackopicko
#    • Hackazon             → /hackazon
#
#  Standalone  (nginx reverse-proxied to a sub-path)
#    • OWASP WebGoat 2023   → /webgoat          (Java, port 8080)
#    • DVNA                 → /dvna             (Node, port 9090)
#    • OWASP Juice Shop     → /juice-shop       (Node, port 3000)
#    • WrongSecrets         → /wrongsecrets     (Java, port 8085)
#
#  Run as root on a Debian/Ubuntu host that already has nginx + PHP-FPM.
# =============================================================================

set -euo pipefail

### ── colour helpers ──────────────────────────────────────────────────────────
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; CYAN='\033[0;36m'; NC='\033[0m'
info()    { echo -e "${CYAN}[INFO]${NC}  $*"; }
success() { echo -e "${GREEN}[OK]${NC}    $*"; }
warn()    { echo -e "${YELLOW}[WARN]${NC}  $*"; }
error()   { echo -e "${RED}[ERR]${NC}   $*"; exit 1; }

### ── sanity checks ───────────────────────────────────────────────────────────
[[ $EUID -ne 0 ]] && error "Please run as root:  sudo bash install.sh"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
WEBROOT="/var/www/html"
LOG_FILE="/var/log/vuln-apps-install.log"

info "Logging to $LOG_FILE"
exec > >(tee -a "$LOG_FILE") 2>&1

### ── detect PHP version ─────────────────────────────────────────────────────
PHP_VER=$(php -r 'echo PHP_MAJOR_VERSION.".".PHP_MINOR_VERSION;' 2>/dev/null || true)
[[ -z "$PHP_VER" ]] && error "PHP not found. Install php-fpm first (apt install php-fpm)."
PHP_FPM_SOCK="/run/php/php${PHP_VER}-fpm.sock"
info "Detected PHP $PHP_VER  (socket: $PHP_FPM_SOCK)"

### ── detect nginx config style ──────────────────────────────────────────────
NGINX_SITES="/etc/nginx/sites-available"
NGINX_ENABLED="/etc/nginx/sites-enabled"

### ── helper: write nginx location block ─────────────────────────────────────
write_nginx_location() {
    local APP_NAME="$1"   # e.g. mutillidae
    local TYPE="$2"       # php | proxy
    local PROXY_PORT="${3:-}"

    local CONF_FILE="${NGINX_SITES}/vuln-apps.conf"

    if [[ ! -f "$CONF_FILE" ]]; then
        cat > "$CONF_FILE" <<NGINXHDR
# Auto-generated by bare-installs/install.sh
# Add your server{} block reference here, or include from your main server block.
# The location blocks below are written for inclusion inside an existing server{} block.
# Run:  sudo cp ${NGINX_SITES}/vuln-apps.conf /etc/nginx/snippets/vuln-apps.conf
#       then add:  include snippets/vuln-apps.conf;  inside your main server {} block.

NGINXHDR
    fi

    if [[ "$TYPE" == "php" ]]; then
        cat >> "$CONF_FILE" <<PHPBLOCK

location /${APP_NAME} {
    alias ${WEBROOT}/${APP_NAME};
    index index.php index.html;
    try_files \$uri \$uri/ /${APP_NAME}/index.php\$is_args\$args;

    location ~ \.php$ {
        fastcgi_pass unix:${PHP_FPM_SOCK};
        fastcgi_index index.php;
        include fastcgi_params;
        fastcgi_param SCRIPT_FILENAME \$request_filename;
    }
}
PHPBLOCK
    elif [[ "$TYPE" == "proxy" ]]; then
        cat >> "$CONF_FILE" <<PROXYBLOCK

location /${APP_NAME}/ {
    proxy_pass http://127.0.0.1:${PROXY_PORT}/${APP_NAME}/;
    proxy_set_header Host \$host;
    proxy_set_header X-Real-IP \$remote_addr;
    proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
    proxy_read_timeout 300s;
}
PROXYBLOCK
    fi
}

### ── helper: enable + start a systemd unit ──────────────────────────────────
enable_service() {
    local NAME="$1"
    systemctl daemon-reload
    systemctl enable --now "$NAME"
    success "Service $NAME started"
}

# ═════════════════════════════════════════════════════════════════════════════
#  SECTION 1 – System dependencies
# ═════════════════════════════════════════════════════════════════════════════
section_deps() {
    info "Installing system dependencies…"
    apt-get update -qq
    apt-get install -y --no-install-recommends \
        git curl wget unzip \
        "php${PHP_VER}" "php${PHP_VER}-fpm" \
        "php${PHP_VER}-mysql" "php${PHP_VER}-xml" "php${PHP_VER}-mbstring" \
        "php${PHP_VER}-curl" "php${PHP_VER}-gd" "php${PHP_VER}-ldap" \
        "php${PHP_VER}-zip" "php${PHP_VER}-bcmath" \
        mysql-server \
        openjdk-17-jre-headless \
        nodejs npm \
        python3 python3-pip

    # Make sure npm/node are reasonably new
    if node --version | grep -qE '^v1[0-5]\.'; then
        warn "Node.js is old; installing LTS via NodeSource…"
        curl -fsSL https://deb.nodesource.com/setup_lts.x | bash -
        apt-get install -y nodejs
    fi

    success "Dependencies ready"
}

# ═════════════════════════════════════════════════════════════════════════════
#  SECTION 2 – MySQL: create databases & users
# ═════════════════════════════════════════════════════════════════════════════
section_mysql() {
    info "Configuring MySQL databases…"
    systemctl start mysql || true

    mysql -u root <<'SQL'
CREATE DATABASE IF NOT EXISTS mutillidae CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
CREATE USER IF NOT EXISTS 'mutillidae'@'localhost' IDENTIFIED BY 'mutillidae_pass';
GRANT ALL PRIVILEGES ON mutillidae.* TO 'mutillidae'@'localhost';

CREATE DATABASE IF NOT EXISTS bwapp CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
CREATE USER IF NOT EXISTS 'bwapp'@'localhost' IDENTIFIED BY 'bwapp_pass';
GRANT ALL PRIVILEGES ON bwapp.* TO 'bwapp'@'localhost';

CREATE DATABASE IF NOT EXISTS hackazon CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
CREATE USER IF NOT EXISTS 'hackazon'@'localhost' IDENTIFIED BY 'hackazon_pass';
GRANT ALL PRIVILEGES ON hackazon.* TO 'hackazon'@'localhost';

FLUSH PRIVILEGES;
SQL
    success "MySQL databases created"
}

# ═════════════════════════════════════════════════════════════════════════════
#  SECTION 3 – Mutillidae II  (PHP)
# ═════════════════════════════════════════════════════════════════════════════
install_mutillidae() {
    info "Installing Mutillidae II…"
    local DEST="${WEBROOT}/mutillidae"

    if [[ -d "$DEST" ]]; then
        warn "Mutillidae already present – skipping clone"
    else
        git clone --depth 1 \
            https://github.com/webpwnized/mutillidae.git \
            "$DEST"
    fi

    # Fix database config
    local CFG="${DEST}/classes/MySQLHandler.php"
    if [[ -f "$CFG" ]]; then
        sed -i "s/'DB_USERNAME', 'root'/'DB_USERNAME', 'mutillidae'/"    "$CFG" || true
        sed -i "s/'DB_PASSWORD', ''/'DB_PASSWORD', 'mutillidae_pass'/"   "$CFG" || true
        sed -i "s/'DB_NAME', 'mutillidae'/'DB_NAME', 'mutillidae'/"      "$CFG" || true
    fi

    chown -R www-data:www-data "$DEST"
    chmod -R 755 "$DEST"

    write_nginx_location "mutillidae" "php"
    success "Mutillidae II installed → http://<ip>/mutillidae"
}

# ═════════════════════════════════════════════════════════════════════════════
#  SECTION 4 – bWAPP  (PHP)
# ═════════════════════════════════════════════════════════════════════════════
install_bwapp() {
    info "Installing bWAPP…"
    local DEST="${WEBROOT}/bwapp"

    if [[ -d "$DEST" ]]; then
        warn "bWAPP already present – skipping"
    else
        git clone --depth 1 \
            https://github.com/jehy-security/bWAPP-docker.git \
            /tmp/bwapp-src
        # the actual PHP lives inside bwapp/ subfolder of that repo
        cp -r /tmp/bwapp-src/bwapp "$DEST" 2>/dev/null || \
            cp -r /tmp/bwapp-src "$DEST"
        rm -rf /tmp/bwapp-src
    fi

    # Patch DB config
    local CFG="${DEST}/admin/settings.php"
    [[ -f "$CFG" ]] && {
        sed -i "s/\$db_username = .*/\$db_username = 'bwapp';/"          "$CFG"
        sed -i "s/\$db_password = .*/\$db_password = 'bwapp_pass';/"     "$CFG"
        sed -i "s/\$db_name = .*/\$db_name = 'bwapp';/"                  "$CFG"
    }

    chown -R www-data:www-data "$DEST"
    chmod -R 755 "$DEST"

    write_nginx_location "bwapp" "php"
    success "bWAPP installed → http://<ip>/bwapp  (run /bwapp/install.php to seed the DB)"
}

